package br.com.impacta.reais;

public class VariaveisReais {
	public static void main(String[] args) {
	
		float f1 = 100.1f; //f ou F para o Java entender que é um float
		double d1 = 100.1;
		float f2 = 100;
		double d2 = 100;
		
		System.out.println("valor do float é " + f1);
		System.out.println("vlaor de double é " + d1);
		System.out.println("valor do float2 é " + f2);
		System.out.println("vlaor de double2 é " + d2);
}
}
